    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        try {
            String username = usernameValue.getText();
            if (username.isEmpty())
                throw new Exception("Vui l√≤ng nh·∫≠p t√™n t√†i kho·∫£n");
            String password = String.copyValueOf(jPasswordValue.getPassword());
            if (password.isEmpty())
                throw new Exception("Vui l√≤ng nh·∫≠p m·∫≠t kh·∫©u");
            String nickName = nicknameValue.getText();
            int avatar = avatarComboBox.getSelectedIndex();
            if (avatar == -1) {
                throw new Exception("Vui l√≤ng ch·ªçn avatar");
            }
            if (nickName.isEmpty())
                throw new Exception("Vui l√≤ng nh·∫≠p nickname");
            Client.closeAllViews();
            Client.openView(Client.View.GAME_NOTICE, "ƒêƒÉng k√≠ t√†i kho·∫£n", "ƒêang ch·ªù ph·∫£n h·ªìi");
            Client.socketHandle.write("register," + username + "," + password + "," + nickName + "," + avatar);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_registerButtonActionPerformed


